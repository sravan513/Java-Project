class AddNum {

    public static void main(String[] args) {
      
      int first = 10;
      int second = 20;
      int third  = 40;
      int fourth = 60;
  
      // add two numbers
      int sum = first + second;
      int sum2 = third + fourth;
      System.out.println(first + " + " + second + " = "  + sum);
      System.out.println(third + " + " + fourth + " = "  + sum2);
    }
  }